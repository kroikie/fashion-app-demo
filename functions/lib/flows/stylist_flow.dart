class SessionState {
  final String sessionId;
  SessionState(this.sessionId);

  DocumentReference<Map<String, dynamic>> get _doc {
    final firestore = FirebaseApp.instance.firestore();
    return firestore.collection('sessions').doc(sessionId);
  }

  Future<Map<String, dynamic>> getAll() async {
    try {
      final snap = await _doc.get();
      if (!snap.exists) return {};
      return snap.data() ?? {};
    } catch (_) {
      return {};
    }
  }

  Future<void> set(String key, dynamic value) async {
    try {
      await _doc.set({key: value}, options: const SetOptions.merge());
    } catch (_) {}
  }

  Future<dynamic> get(String key) async {
    final all = await getAll();
    return all[key];
  }
}

final getProductImageTool = ai.defineTool(
  name: 'getProductImage',
  description: 'get the URL or identifier for a product image',
  inputSchema: SchemanticType.from(
    jsonSchema: {
      'type': 'object',
      'properties': {
        'image_name': {'type': 'string'}
      },
      'required': ['image_name']
    },
    parse: (json) => json,
  ),
  outputSchema: SchemanticType.from(
    jsonSchema: {
      'type': 'object',
      'properties': {
        'image': {'type': 'string'}
      },
      'required': ['image']
    },
    parse: (json) => json,
  ),
  fn: (input, context) async {
    final imageName = input['image_name'] as String;
    return {'image': imageName};
  },
);

